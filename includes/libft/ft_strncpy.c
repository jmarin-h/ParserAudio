#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char	*dest2;
	size_t	i;

	i = 0;
	dest2 = dest;
	while (i < n && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest2);
}

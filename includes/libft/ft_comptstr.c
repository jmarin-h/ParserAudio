#include "libft.h"

int		ft_comptstr(char *str, char c)
{
	int		i;
	int		cp;

	i = 0;
	cp = 0;
	while (str[i])
	{
		if (str[i] != c)
		{
			cp++;
			while (str[i] && str[i] != c)
				i++;
		}
		i++;
	}
	return (cp);
}
